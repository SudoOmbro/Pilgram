class ItemStats:

    def __init__(self):
        self.pierce: float = 1
        self.slash: float = 1
        self.strike: float = 1
        self.fire: float = 1
        self.ice: float = 1
        self.dark: float = 1
        self.holy: float = 1
        self.weight: float = 1


class EntityStats:

    def __init__(self):
        self.str: float = 1  # strength
        self.dex: float = 1  # dexterity
        self.int: float = 1  # intelligence
        self.chr: float = 1  # charisma

